  name: "Slider",
  components: {
    swiper,
    swiperSlide,
  },
  props: {
    cname: {
      type: String,
      default: "",
    },
    options: {
      type: Object,
      default() {
        return {
          autoplay: true,
          loop: true,
          pagination: {
            el: ".swiper-pagination",
